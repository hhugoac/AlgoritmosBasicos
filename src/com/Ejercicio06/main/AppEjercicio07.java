package com.Ejercicio06.main;
import java.util.Scanner;

public class AppEjercicio07 {

	public static void main(String[] args) 
	{
		// Inicio de las variables 
		int i;
		
		// Ciclo for
		for(i=1;i<=50;i++)
		{
			System.out.println(i);
		}		
		
		// Ciclo while
		i=1;
		while(i<=50)
		{
			System.out.println(i);
			i++;
		}
		
		// Ciclo do...while
		
		i=1;
		do
		{
			System.out.println(i);
			i++;
		}while(i<=50);
		
		
		
	}

}
